/**
  * @brief This function handles TIM1 update interrupt.
  */
void TIM1_UP_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_UP_IRQn 0 */
  Time.disp_blink_time++;

  if(g_system_status.key_event_flag)
  {
	  Time.keypad_time++;
  }

  if(Time.key_input_delay)
  {
	  Time.key_input_delay--;
  }

  if(Time.wifi_connect_check_delay)
  {
	  Time.wifi_connect_check_delay--;
  }

  if(Time.temp_humid_check_delay)
  {
	  Time.temp_humid_check_delay--;
  }

  if(Time.disp_brightness_check_delay)
  {
	  Time.disp_brightness_check_delay--;
  }

  if(Time.beep_delay)
  {
	  Time.beep_delay--;
  }
  else
  {
	  SET_DUTY(1999);
  }

  /* USER CODE END TIM1_UP_IRQn 0 */
  HAL_TIM_IRQHandler(&htim1);
  /* USER CODE BEGIN TIM1_UP_IRQn 1 */

  /* USER CODE END TIM1_UP_IRQn 1 */
}

