static char tail_buf[7] = {0};

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	//static uint16_t response_parse_flag = FALSE;
	static uint16_t response_length = 0;

	if(huart->Instance == USART1)
	{
		g_wifi_buf.buf[g_wifi_buf.head++] = g_wifi_buf.rx_data;

        tail_buf[0] = tail_buf[1];
        tail_buf[1] = tail_buf[2];
        tail_buf[2] = tail_buf[3];
        tail_buf[3] = tail_buf[4];
        tail_buf[4] = tail_buf[5];
        tail_buf[5] = tail_buf[6];
        tail_buf[6] = g_wifi_buf.rx_data;

        if (strncmp(&tail_buf[3], "OK\r\n", 4) == 0)
        {
        	g_wifi_packet++;
        }
        else if(strncmp(&tail_buf[0], "+IPD,", 5) == 0) //incomming packet data
		{
        	 response_length = (tail_buf[5]-'0')*10 + (tail_buf[6]-'0');
        	 //response_parse_flag = TRUE;
		}

        if(response_length)
        {
        	response_length--;
        	if(response_length == 0)
        	{
        		//response_parse_flag = FALSE;
        		g_wifi_packet++;
        	}
        }

        if(g_wifi_buf.head == MAX_BUFFER_SIZE)
		{
			g_wifi_buf.head = 0;
		}

		HAL_UART_Receive_IT(&huart1, &g_wifi_buf.rx_data, 1);
	}
}
